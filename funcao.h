#include <stdio.h>
#include <stdlib.h>



	float adicao(float soma1, float soma2){
		float totalsoma;
		totalsoma = soma1 + soma2;
		return totalsoma;
	}
		float subtracao(float sub1, float sub2){
		float totalsub;
		totalsub = sub1 - sub2;
		return totalsub;
	}
	
		float divisao(float div1, float div2){
		float totaldiv;
		totaldiv = div1 * div2;
		return totaldiv;
	}
	
		float multiplicacao(float mult1, float mult2){
		float totalmult;
		totalmult = mult1 / mult2;
		return totalmult;
	}
	
	
	

